char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s)
	{
		i++;
		s++;
	}
	i = i + 1;
	while (i)
	{
		if (*s == (char)c)
			return ((char *)s);
		i--;
		s--;
	}
	return (0);
}
